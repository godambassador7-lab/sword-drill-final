/**
 * Get a random verse reference from a specific difficulty level
 * @param {string} difficulty - "Beginner", "Intermediate", or "Advanced"
 * @returns {string} A random verse reference
 */
export function getRandomReferenceByDifficulty(difficulty = "Beginner") {
  const verses = VERSES_BY_DIFFICULTY[difficulty] || VERSES_BY_DIFFICULTY.Beginner;
  return verses[Math.floor(Math.random() * verses.length)];
}

/**
 * Get all verse references for a difficulty level
 * @param {string} difficulty - "Beginner", "Intermediate", or "Advanced"
 * @returns {Array<string>} Array of verse references
 */
export function getAllReferencesForDifficulty(difficulty = "Beginner") {
  return VERSES_BY_DIFFICULTY[difficulty] || VERSES_BY_DIFFICULTY.Beginner;
}

/**
 * Check if a reference exists in a difficulty level
 * @param {string} reference - Verse reference to check
 * @param {string} difficulty - Difficulty level
 * @returns {boolean} True if reference exists in that difficulty
 */
export function isReferenceInDifficulty(reference, difficulty) {
  const verses = VERSES_BY_DIFFICULTY[difficulty];
  if (!verses) return false;
  return verses.some(ref => ref === reference || reference.startsWith(ref));
}

export default {
  VERSES_BY_DIFFICULTY,
  getRandomReferenceByDifficulty,
  getAllReferencesForDifficulty,
  isReferenceInDifficulty
};
